/*

Array methods (JavaScript)

Array methods are built-in functions that help you add, remove, change, search, or loop through array elements.

*/

// ! Common Array Methods (most used)

//! 1️⃣ push() – add at end (changes original array)

let a = [1, 2, 3];
a.push(4);
console.log(a); // [1, 2, 3, 4]

//! 2️⃣ pop() – remove from end (changes original array)

let b = [1, 2, 3];
b.pop();
console.log(b); // [1, 2]

// ! 3️⃣ unshift() – add at start

let c = [2, 3];
c.unshift(1);
console.log(c); // [1, 2, 3]


// ! 4️⃣ shift() – remove from start

let d = [1, 2, 3];
d.shift();
console.log(d); // [2, 3]




